package _05_operadores

fun main() {
    println("10 == 10 = ${10 == 10}")
    println("10 != 10 = ${10 != 10}")
    println()

    println("4 == 5 = ${4 == 5}")
    println("4 != 5 = ${4 != 5}")
    println()

    println("2 < 3 = ${2 < 3}")
    println("3 < 2 = ${3 < 2}")
    println("3 < 3 = ${3 < 3}")
    println()

    println("3 > 2 = ${3 > 2}")
    println("2 > 3 = ${2 > 3}")
    println("3 > 3 = ${3 > 3}")
    println()

    println("2 <= 3 = ${2 <= 3}")
    println("3 <= 2 = ${3 <= 2}")
    println("3 <= 3 = ${3 <= 3}")
    println()

    println("3 >= 2 = ${3 >= 2}")
    println("2 >= 3 = ${2 >= 3}")
    println("3 >= 3 = ${3 >= 3}")
    println()

    println("true equivale a 1")
    println("false equivale a 0")
    println("true > false = ${true > false}")
    println("false > true = ${false > true}")
}