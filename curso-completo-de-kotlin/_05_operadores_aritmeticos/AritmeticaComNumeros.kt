package _05_operadores_aritmeticos

fun main() {
    var n1 = 10
    var n2 = 20
    var n3 = 10.0f

    var soma = n1 + n2
    var subtracao = n1 - n2
    var multiplicacao = n1 * n2
    var divisaoInteira = n1 / n2
    var divisao: Float = n3 / n2

    println(soma)
    println(subtracao)
    println(multiplicacao)
    println(divisaoInteira)
    println(divisao)
}